     public void creeMaps ()
    {
     
        if(!jTextField2.getText().isEmpty()){
            try {
                
                this.ObjStaticMaps.setLanguage("fr");
                this.ObjStaticMaps.setRegion("tn");
                this.JLabel_ME_Imagen.setText("");
                Image imagenMapa=ObjStaticMaps.getStaticMap(jTextField2.getText(),
                        15,new Dimension(500,500),
                        1,StaticMaps.Format.png,
                        StaticMaps.Maptype.roadmap);
               if(imagenMapa!=null){
                   ImageIcon imgIcon=new ImageIcon(imagenMapa);
                   Icon iconImage=(Icon)imgIcon;
                   JLabel_ME_Imagen.setIcon(iconImage);
               }
            } catch (MalformedURLException ex) {
                Logger.getLogger(DetailsHotel.class.getName()).log(Level.SEVERE, null, ex);
            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(DetailsHotel.class.getName()).log(Level.SEVERE, null, ex);
            }
    }}
